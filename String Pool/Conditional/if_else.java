class Conditional {
	public static void main(String[] args) {
		boolean a = true;
		if(a) {
			System.out.println("Hello");
		}
		else {
			System.out.println("Bye");
		}
	}
}		

class Condition {
	public static void main(String[] args) {
		int a = 10, b = 5;
		if(a<b) {
			System.out.println("The condition is true");
		}
		else {
			System.out.println("The condition is false");
		}
	}
}
