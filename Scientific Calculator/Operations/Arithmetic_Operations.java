package Operations;
import java.util.*;

public class Arithmetic_Operations {
	
	public void Addition() {
		Scanner sin = new Scanner(System.in);
		double a, b;
		System.out.println("Enter Two Numbers");
		a = sin.nextDouble();
		b = sin.nextDouble();
		
		System.out.println(a + b);
	}
	
	public void Subtraction() {
		Scanner sin = new Scanner(System.in);
		double a, b;
		System.out.println("Enter Two Numbers");
		a = sin.nextDouble();
		b = sin.nextDouble();
		
		System.out.println(a - b);
	}
	
	public void Multiplication() {
		Scanner sin = new Scanner(System.in);
		double a, b;
		System.out.println("Enter Two Numbers");
		a = sin.nextDouble();
		b = sin.nextDouble();
		
		System.out.println(a * b);
	}
	
	public void Divison() {
		Scanner sin = new Scanner(System.in);
		double a, b;
		System.out.println("Enter Two Numbers");
		a = sin.nextDouble();
		b = sin.nextDouble();
		
		System.out.println(a / b); 
	}
}