package XYZ;

public class Arithmetic_Operations {
	public void Add(int a,  int b) {
		int c = a + b;
		System.out.println("The addition of the two numbers" + a + " and " + b + " is " + c);
	}
	public void Sub(int a, int b) {
		int c = a - b;
		System.out.println("The subtraction of the two numbers" + a + " and " + b + " is " + c);
	}
	public void Mul(int a, int b) {
		int c = a * b;
		System.out.println("The multiplication of the two numbers" + a + " and " + b + " is " + c);
	}
	public void Div(int a, int b) {
		double c = a / b;
		System.out.println("The divison of the two numbers" + a + " and " + b + " is " + c);
	}
}